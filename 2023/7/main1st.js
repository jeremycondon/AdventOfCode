const scoreOrder = ['A', 'K', 'Q', 'J', 'T', '9', '8', '7', '6', '5', '4', '3', '2'].reverse()

function calcCardsScore(cards) {

    let sortedCards = cards.split('').sort((a, b) => {
        if (a < b) {
            return -1
        }
        return 1;
    })

    lastCard = sortedCards[0]
    score = 0;
    threeKind = 0
    twoKind = 0;
    curCount = 0;
    for (i = 0; i < sortedCards.length; i++) {
        if (sortedCards[i] == lastCard) {
            curCount++
        }
        else {
            if (curCount === 2) {
                twoKind++
            }
            if (curCount === 3) {
                threeKind = 1
            }
            curCount = 1
            lastCard = sortedCards[i]

        }
        score = Math.max(score, curCount)
    }

    if (curCount === 2) {
        twoKind++
    }
    if (curCount === 3) {
        threeKind = 1
    }

    if (twoKind == 1 && threeKind == 1) {
        return 5
    }
    switch (score) {
        case 1: return 1
        case 2: return twoKind + 1 // either 1 or 2 of a kind
        case 3: return 4
        case 4: return 6
        case 5: return 7
    }
}

